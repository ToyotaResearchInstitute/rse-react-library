import * as React from "react"

import { cn } from "./utils"

/**
 * TRI Card. Per the design-system Cards preview, the default surface is an
 * outlined card: 1px #e5e5e5 hairline border on white, 12px radius
 * (`rounded-lg`), 24px padding (`p-6`) and a resting `shadow-sm`. Composed of
 * plain div sub-parts so it can wrap media, KPI tiles, articles, etc.
 */
const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-lg border border-border bg-card text-foreground shadow-sm",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

